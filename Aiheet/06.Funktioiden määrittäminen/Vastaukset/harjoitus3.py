# Harjoitus 3: Funktio return-arvolla
# Tavoite: Harjoittele return-avainsanaa ja palautusarvoja

# 1. Määritä funktio nelio(luku) joka palauttaa luvun neliön
def nelio(luku):
    return luku * luku

# 2. Kutsu funktiota luvuilla 3, 5 ja 10 ja tulosta tulokset
print(nelio(3))
print(nelio(5))
print(nelio(10))
